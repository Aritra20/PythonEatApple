import statistics as s 
mydata=[1,2,3,4,5,6,7,8,9]
print("The number is",mydata)
print("The sum of the number is",sum(mydata))
print("The mean of the number is",s.mean(mydata))
print("The median of the number is",s.median(mydata))
print("The mode of the number is",s.mode(mydata))
print("The population standard deviation of the number is",s.pstdev(mydata))
print("The standard deviation of the number is",s.mean(mydata))
print("The variance of the number is",s.variance(mydata))
print("The population variance of the number is",s.pvariance(mydata))